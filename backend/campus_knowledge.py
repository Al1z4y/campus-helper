def get_context_by_category(category=None):
    """Returns specific sections of knowledge base for targeted queries"""
    sections = {
        'academic': ['📚 ACADEMIC STRUCTURE', '📝 ACADEMIC PROCEDURES'],
        'facilities': ['🏢 BUILDINGS AND LOCATIONS', '💻 TECHNOLOGY & DIGITAL SERVICES'],
        'policies': ['📋 CAMPUS POLICIES & PROCEDURES'],
        'student_life': ['🏃 STUDENT LIFE & ACTIVITIES', '🍽️ FOOD & DINING'],
        'emergency': ['🚨 EMERGENCY & SUPPORT SERVICES'],
        'contacts': ['📞 IMPORTANT CONTACTS & EXTENSIONS'],
        'faq': ['❓ FREQUENTLY ASKED QUESTIONS (EXPANDED)']
    }
    
    if not category:
        return CAMPUS_KNOWLEDGE
    
    relevant_sections = []
    lines = CAMPUS_KNOWLEDGE.split('\n')
    in_section = False
    current_section = ""
    
    for line in lines:
        if any(line.startswith(section) for section in sections.get(category, [])):
            in_section = True
            current_section = line
            relevant_sections.append(line)
        elif in_section and line.startswith('## ') and line != current_section:
            in_section = False
        elif in_section:
            relevant_sections.append(line)
    
    return '\n'.join(relevant_sections)


